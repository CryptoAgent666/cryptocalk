export function getUiString(lang: string | undefined, key: string): string {
    if (!lang || lang === 'en') return key;
    const targetLang = lang as Exclude<Lang, 'en'>;
    if (!uiStrings[targetLang]) return key;
    return uiStrings[targetLang][key] || key;
}
